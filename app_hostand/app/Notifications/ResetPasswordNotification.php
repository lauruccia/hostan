<?php

namespace App\Notifications;

use Illuminate\Bus\Queueable;
use Illuminate\Notifications\Notification;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Notifications\Messages\MailMessage;

class ResetPasswordNotification extends Notification
{
    use Queueable;

    public $token;

    /**
     * Create a new notification instance.
     *
     * @param string $token
     * @return void
     */
    public function __construct($token)
    {
        $this->token = $token;
    }

    /**
     * Get the notification's delivery channels.
     *
     * @param mixed $notifiable
     * @return array
     */
    public function via($notifiable)
    {
        return ['mail'];
    }

    /**
     * Get the mail representation of the notification.
     *
     * @param mixed $notifiable
     * @return \Illuminate\Notifications\Messages\MailMessage
     */
    public function toMail($notifiable)
    {
        $url = url(route('password.reset', [
            'token' => $this->token,
            'email' => $notifiable->getEmailForPasswordReset(),
        ], false));

        return (new MailMessage)
            ->subject('Reimpostazione Password - Hostand')
            ->greeting('Ciao ' . $notifiable->name . '!')
            ->line('Hai ricevuto questa email perché abbiamo ricevuto una richiesta di reimpostazione password per il tuo account.')
            ->action('Reimposta Password', $url)
            ->line('Questo link per reimpostare la password scadrà tra ' . config('auth.passwords.'.config('auth.defaults.passwords').'.expire') . ' minuti.')
            ->line('Se non hai richiesto una reimpostazione della password, non è necessaria alcuna azione.')
            ->salutation('Cordiali saluti, il team di Hostand');
    }

    /**
     * Get the array representation of the notification.
     *
     * @param mixed $notifiable
     * @return array
     */
    public function toArray($notifiable)
    {
        return [
            //
        ];
    }
}
